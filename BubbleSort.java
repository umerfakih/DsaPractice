public class BubbleSort {

    public static void main(String[] args) {

        int[] arr = {7, 4, 1, 5, 3};

        bubbleSort(arr);   // ✅ call the method

        // ✅ print the sorted array
        for (int num : arr) {
            System.out.print(num + " ");
        }
    }

    public static void bubbleSort(int[] arr) {
        int n = arr.length;

        for (int i = 0; i < n - 1; i++) {
            for (int j = 0; j < n - i - 1; j++) {
                if (arr[j] > arr[j + 1]) {
                    int temp = arr[j + 1];
                    arr[j + 1] = arr[j];
                    arr[j] = temp;
                }
            }
        }
    }
}
